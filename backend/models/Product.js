const mongoose = require('mongoose');

const productSchema = new mongoose.Schema(
    {
        // This links the product to the specific Seller or Admin who created it
        user: {
            type: mongoose.Schema.Types.ObjectId,
            required: true,
            ref: 'User',
        },
        name: {
            type: String,
            required: true,
        },
        image: {
            type: String,
            required: true,
        },
        brand: {
            type: String,
            required: true,
        },
        category: {
            type: String,
            required: true,
        },
        description: {
            type: String,
            required: true,
        },
        price: {
            type: Number,
            required: true,
            default: 0,
        },
        countInStock: {
            type: Number,
            required: true,
            default: 0,
        },
    },
    {
        timestamps: true, // Automatically creates 'createdAt' and 'updatedAt'
    }
);

module.exports = mongoose.model('Product', productSchema);